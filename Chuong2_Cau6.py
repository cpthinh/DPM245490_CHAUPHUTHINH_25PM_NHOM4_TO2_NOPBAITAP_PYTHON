#/: toán tử chia lấy kết quả là số thực
#//: toán tử chia lấy phần nguyên
#%: toán tử chia lấy phần dư
#**: toán tử luỷ thừa
#and: toán tử logic, trả về True nếu cả hai toán hạng đều đúng, ngược lại trả về giá trị False
#or: toán tử logic, trả về True nếu ít nhất 1 trong 2 toán hạng đúng, ngược lại trả về giá trị False
#is: toán tử so sánh, trả về True nếu 2 toán hạng cùng tham chiếu đến 1 đối tượng, ngược lại trả về False
